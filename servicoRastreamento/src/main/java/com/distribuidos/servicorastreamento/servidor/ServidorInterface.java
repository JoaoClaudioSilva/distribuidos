    public void atualizarTabela(ArrayList<JSONObject> listaDePacotes){
        DefaultTableModel model = (DefaultTableModel) tblPacotes.getModel();
        
        model.setRowCount(0);
        
        Iterator iterator = listaDePacotes.iterator();
        
        while(iterator.hasNext()){
            JSONObject pacote = (JSONObject)iterator.next();
            model.addRow(new Object[]
            {
                pacote.get("codigoRastreio"),
                pacote.get("dataEnvio"),
                pacote.get("pesoPacote"),
                pacote.get("origemPacote"),
                pacote.get("destinoPacote"),
            });
        }
    }
    

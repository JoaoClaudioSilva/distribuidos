    private void btnAdicionarPacoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarPacoteActionPerformed
        boolean isValid;
        
        try
        {
            pesoPacote = Float.valueOf(txtPesoPacote.getText());
            origemPacote = txtOrigemPacote.getText().toUpperCase();
            destinoPacote = txtDestinoPacote.getText().toUpperCase();
            isValid = true;
        }
        catch(NumberFormatException ex)
        {
            Logger.getLogger(ServidorInterface.class.getName()).log(Level.SEVERE, "Erro ao cadastrar pacote", ex);
            isValid = false;
        }
        
        if(origemPacote.toString().equals("") || destinoPacote.toString().equals(""))
            isValid = false;
            
        
        txtPesoPacote.setText("");
        txtOrigemPacote.setText("");
        txtDestinoPacote.setText("");
        
        if(isValid){
            JSONObject pacote = new JSONObject();
        
            long codigoRastreio = (long)(new Random().nextDouble() * Math.pow(10, 10));

            pacote.put("dataEnvio", new Date().toString());
            pacote.put("codigoRastreio", codigoRastreio);
            pacote.put("pesoPacote", pesoPacote);
            pacote.put("origemPacote", origemPacote);
            pacote.put("destinoPacote", destinoPacote);

            servidor.addPacote(pacote);
        }
        else
        {
            ErroMensagens.avisoErro("As informações do pacote não são válidas");
        }
    }//GEN-LAST:event_btnAdicionarPacoteActionPerformed


    private void btnRastrearPacoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRastrearPacoteActionPerformed
        long codigoRastreio;        

        try
        {
            codigoRastreio = Long.valueOf(txtCodigoRastreio.getText());
            cliente.consultarCodigo(codigoRastreio);
        }
        catch (NumberFormatException ex)
        {
            String msg = "Número de rastreio vazio";
            Logger.getLogger(ClienteInterface.class.getName()).log(Level.SEVERE, msg, ex);
            ErroMensagens.avisoErro(msg);
        }
        
        txtCodigoRastreio.setText("");
    }//GEN-LAST:event_btnRastrearPacoteActionPerformed

    private void cmbCodigoPacoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbCodigoPacoteActionPerformed
        String itemSelecionado = cmbCodigoPacote.getSelectedItem().toString(); 
        ArrayList<JSONObject> listaDePacotes = cliente.getListaDePacotes();
        DefaultTableModel model = (DefaultTableModel)tblPacote.getModel();
        
        model.setRowCount(0);
        
        Iterator<JSONObject> iterator = listaDePacotes.iterator();
        
        while (iterator.hasNext()) {
            JSONObject pacote = iterator.next();
            if(pacote.get("codigoRastreio").toString().equals(itemSelecionado))
                model.addRow(new Object[]
                {
                    pacote.get("origemPacote"),
                    "Cadastrado",
                    pacote.get("dataEnvio")
                });
        }
    }//GEN-LAST:event_cmbCodigoPacoteActionPerformed


    

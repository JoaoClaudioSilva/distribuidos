    public void atualizarCombo(ArrayList<JSONObject> listaDePacotes){
        
        cmbCodigoPacote.removeAllItems();
        
        Iterator<JSONObject> iterator = listaDePacotes.iterator();
        
        while(iterator.hasNext()){
            JSONObject pacote = iterator.next();
            cmbCodigoPacote.addItem(pacote.get("codigoRastreio").toString());
        }
    }
    

